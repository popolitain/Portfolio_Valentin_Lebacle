    <h1>"Présentation du BTS SIO"</h1>

    <section id="presentation">
        <p>Le BTS SIO (Services Informatiques aux Organisations) est un diplôme de niveau Bac+2 qui forme des
            professionnels de l'informatique capables de répondre aux besoins des entreprises en matière de services
            informatiques.</p>
        <p>Le BTS SIO propose deux options :</p>
        <ul>
            <li>L'option SISR (Solutions d'Infrastructure, Systèmes et Réseaux) qui forme des techniciens capables de
                concevoir, mettre en place et gérer des infrastructures informatiques</li>
            <li>L'option SLAM (Solutions Logicielles et Applications Métiers) qui forme des techniciens capables de
                développer des applications métiers et de gérer des projets informatiques</li>
        </ul>
    </section>

    <section id="sisr">
        <h2>Option SISR</h2>
        <p>L'option SISR (Solutions d'Infrastructure, Systèmes et Réseaux) forme des techniciens capables de :</p>
        <ul>
            <li>concevoir et mettre en place des architectures réseaux</li>
            <li>administrer des serveurs et des services réseaux</li>
            <li>assurer la sécurité des systèmes d'information</li>
        </ul>
    </section>

    <section id="slam">
        <h2>Option SLAM</h2>
        <p>L'option SLAM (Solutions Logicielles et Applications Métiers) forme des techniciens capables de :</p>
        <ul>
            <li>concevoir et développer des applications métiers</li>
            <li>assurer la maintenance et l'évolution des applications</li>
            <li>gérer des projets informatiques</li>
        </ul>
    </section>
